public class Clase_Panel {

    private int X;
    private int Y; 
    private int H;
    private int W;

    public Clase_Panel(int X, int Y,int H,int W){

        this.X = X;
        this.Y = Y;
        this.H = H;
        this.W = W;
    }

    public int get_X(){
        return X;
    }
    public int get_Y(){
        return Y;
    }
    public int get_H(){
        return H;
    }
    public int get_W(){
        return W;
    }


}
